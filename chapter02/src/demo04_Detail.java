public class demo04_Detail {
    public static void main(String[] args) {
        //long 数据类型要在后面加L
        byte b = 1;
        short s = 2;
        int i = 3;
        long l = 4L;

        //float数据类型要在后面加F
        //浮点数 = 符号位 + 指数位 + 尾数位
        //尾数位可能会丢失
        float f = 3.14F;
        double d = 9.98;


    }
}
